from pyfiglet import Figlet
from rich import print

def get_opennig():
    program_name = "Anime Installer Master"
    f = Figlet(font='starwars')

    # إنشاء النص مع إطار
    figlet_text = f.renderText(program_name).rstrip().split('\n')
    width = max(len(line) for line in figlet_text) + 6
    print('*' * width)
    print('*' + ' ' * (width-2) + '*')
    for line in figlet_text:
        print(f'*  {line:<{width-6}}  *')
    print('*' + ' ' * (width-2) + '*')
    print('*' * width)


